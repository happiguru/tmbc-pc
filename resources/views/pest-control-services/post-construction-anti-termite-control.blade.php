                    <h3><strong>POST CONSTRUCTION ANTI-TERMITE CONTROL</strong></h3>
                    <h4><strong>Post-Construction Subterranean Termite Control</strong></h4>
                    <p>
                        Subterranean Termites &ldquo;nest&rdquo; in the soil and from there they can attack structures by building shelter tubes from the soil to the wood in structures. To control termites, it is almost always necessary to
                        use pesticides. Pesticides used to control termites are called termiticides. Termiticides may be applied as liquids or baits. When applied as a liquid, the termiticide is injected into the soil or to wood members of
                        a structure. When a bait system is used, the bait is placed in specially designed bait stations in the soil around the exterior perimeter of a structure. In this publication, we will discuss some of the options that
                        homeowners&rsquo; can use to better understand how a structure is treated for termites.
                    </p>
                    <p>
                        <strong>TREATMENT OF STRUCTURES</strong><br />
                        A termite treatment may involve any of the following basic steps:
                    </p>
                    <ul>
                        <li>Mechanical alteration/sanitation</li>
                        <li>Soil treatment</li>
                        <li>Wood treatment</li>
                        <li>Foundation treatment</li>
                        <li>Bait/monitoring system</li>
                    </ul>
                    <h4><strong>Mechanical alteration /sanitation</strong></h4>
                    <p>
                        Wood, paper, cardboard, and other cellulose debris under or against a structure increase the risk of termite infestation. Similarly, wood supports, fence posts etc. in contact with the soil and the structure present
                        easy access for termite entry. Regardless of what treatment options are used, these items should be corrected. Debris must be removed and wood /soil contacts should be broken. Termites thrive in moist environments
                        and can survive above ground in excessively wet wood.
                    </p>
                    <p>
                        Correcting plumbing and roof leaks and other defects contributing to such conditions is imperative. Increasing ventilation in the crawlspace, adding rain gutters, grading to direct surface water away from the house
                        may be beneficial in resolving moisture conditions. Mechanical alteration/sanitation techniques alone are rarely sufficient to prevent or control a termite infestation.
                    </p>
                    <h4><strong>Soil treatment&nbsp;</strong></h4>
                    <p>
                        Treatment of the soil establishes a termiticide barrier in the soil under and adjacent to a building. A continuous barrier must be established along the inside and outside of the foundation wall, under slabs and
                        around utility entrances.
                    </p>
                    <p>
                        A&nbsp;<em><strong>vertical</strong></em>&nbsp;barrier is established in the soil by trenching or trenching and rodding along all sides of foundation elements such as foundation walls, chimney bases, pilasters, and
                        pillars. The trench must be at least 6 inches in depth. Termiticide is applied by trenching or trenching and rodding from the top of the grade to the top of the footing or to a&nbsp;
                        <em><strong>minimum depth of 30 inches</strong></em><strong>.&nbsp;</strong>Where drain tile, french drains, or other foundation drainage systems present a hazard of contamination outside the treatment zone,
                        treatment shall be performed in a manner that will not introduce the termiticide into the drainage system. Where footings are exposed, the treatment is performed next to the footing but not below the bottom of the
                        footing. Soil around sewer pipes and conduits and all other structural members in contact with the soil must also be treated. The insecticide must be mixed with water as required on the pesticide label and applied at
                        the rate of 4 gallons per 10 linear feet, per foot of depth.
                    </p>
                    <p>
                        It is possible to trench around the outside of a slab after it has been poured, but this alone usually will not give satisfactory termite control because the termite colony may be entering the structure from the soil
                        under the slab. As previously mentioned, (see pre-construction termite control brochure) a slab will crack or shrink away from the foundation wall allowing termites to infest the wood above. In addition, concrete
                        slabs usually have many other points of entry such bath traps, plumbing outlets, etc.
                    </p>
                    <p>
                        Slab construction requires a lot of time and labour when treating for termites. Slab construction often will require precise drilling to block all termite entry points. Slabs must be drilled vertically along all
                        cracks and construction joints at 12-inch intervals and no more than 6 inches from the foundation. The soil below the slab must be treated from the bottom of the slab to the top of the footing. This method of
                        drilling and treating is also used for dirt-filled porches and stress cracks sometimes found in slabs.
                    </p>
                    <h4><strong>Wood treatment</strong></h4>
                    <p>
                        This type of treatment involves applications of termiticides directly to wood to eliminate existing termite infestations or to make the wood resistant to termites. There are several supplemental ways in which wood
                        treatments are used in the pest control industry.
                    </p>
                    <p>Pressure-treated wood is frequently used in the construction of buildings and provides effective termite control if it is used for all wood construction, at least to the ceiling level of the first floor.</p>
                    <p>Spraying termiticides on the wood already in place provide only surface protection and don&rsquo;t penetrate to the centre of the wood, where it is most needed.</p>
                    <p>In damaged wood, termiticide can be injected into the cavities made by termites. This will provide better control than will a brush or spray application.</p>
                    <h4><strong>Foundation treatment</strong></h4>
                    <p>
                        Foundation treatments involve the application of termiticides to foundation elements. The objective of this treatment is to create a barrier by placing termiticides inside of concrete block/multiple brick walls where
                        voids exist. This is accomplished by drilling foundation elements and injecting termiticides. Drilling and treating foundation elements allows termiticides to be placed on top of concrete footings where cracks may
                        exist. In addition, where evidence of either past or present subterranean termite infestation exists, voids in multiple masonry foundation elements must be drilled and treated at a minimum distance of four feet in
                        all directions from such evidence.
                    </p>
                    <h4><strong>Baits/monitoring systems&nbsp;</strong></h4>
                    <p>
                        These systems are a recent innovation in termite control. Termiticide baits control termites by eliminating or reducing the size of the termite colony. They do not create a barrier around the structure, as do the
                        liquid insecticides discussed earlier. Presently, termiticide baits are either insect growth regulators (IGRs) or slow acting poisons. The use of termiticide baits can significantly reduce or eliminate the need for a
                        conventional liquid insecticide.
                    </p>
                    <p>
                        There are two strategies for the use of termiticide baits. One involves the placement of monitoring devices containing untreated wood or other cellulose material in the soil around the structure to be protected. When
                        termites are detected in the monitoring device, the untreated cellulose material is replaced with a treated material, referred to as a &ldquo;termiticide bait&rdquo;. The bait material is replenished as long as
                        termites continue feeding. For the bait system to be effective, the proper number of stations must be installed and the stations must be inspected or monitored regularly as indicated on the product label. When
                        termites stop feeding, the bait is replaced with untreated wood or other cellulose material. As a variation of this strategy, devices may be pre-baited and placed in the soil in areas where termites are present.
                    </p>
                    <p>
                        The second bait strategy currently in use involves the attachment of bait devices directly to the surface of construction elements where termites are actively feeding. These devices are pre-baited and are attached to
                        the foundation walls, floor joists, sub-floor and other similar locations. Termites then feed on the bait material.
                    </p>
                    <p>
                        There are advantages and disadvantages to the use of termiticide baits. Baits may be used to treat structures that cannot be treated with liquid termiticides due to the presence of a well, inaccessibility of infested
                        areas or concerns about the use of pesticides. However, in most cases, termiticide baits do not provide quick control of the termite infestation. In addition, the maintenance of bait system service agreements is
                        normally more expensive than maintenance of conventional treatment warranties.
                    </p>
                    <h4><strong>Waiver Form</strong></h4>
                    <p>
                        Whenever possible, it is best to treat a structure completely in accordance with the Structural Pest Control Rules. The North Carolina Official Waiver of Minimum Requirements for the Control of Subterranean Termites
                        in Existing Structures is a standard form prescribed by the NC Structural Pest Control Committee. This form permits the licensee to deviate from or omit one or more of the minimum subterranean treatment requirements.
                        For example, a waiver may be necessary because the working clearance in the crawlspace is inadequate for it to be treated properly, or a treatment requires indoor drilling that may not be desirable.
                    </p>
                    <p>
                        This latter situation usually occurs with slab construction, where holes must be drilled through the floor or the floor must be removed in order to drill the slab and treat the soil beneath it. Any part of the
                        treatment that is waived must be properly explained on the standard waiver form. The waiver form is not required for termite baits/monitoring systems as long as the pest control company provides a service agreement
                        and/or warranty for the entire structure treated.
                    </p>
                    <p>
                        <strong>Conclusion</strong><br />
                        Remember the best method for the control of subterranean termites is prevention. Listed below are some options that should be used in termite control.
                    </p>
                    <h4><strong>Preventive measures should include:</strong></h4>
                    <div>
                        <ul>
                            <li>Remove all stumps, dead wood, and other cellulose containing material in contact with the soil from the crawl space</li>
                        </ul>
                        <ul>
                            <li>Remove all form boards and grade stakes</li>
                        </ul>
                        <ul>
                            <li>
                                There should be no contact between the building woodwork and the soil or fill material. Exterior woodwork should be located a minimum of 6 inches above the ground and beams in crawl spaces at least 18 inches
                                above ground to provide ample space to make future inspections
                            </li>
                        </ul>
                        <ul>
                            <li>Ventilation openings in foundations should be designed to prevent dead air pockets. This helps keep the ground dry and unfavourable for termites</li>
                        </ul>
                        <ul>
                            <li>Thorough annual inspections should be conducted to discover evidence of termite activity such as shelter tubes on foundation surfaces, discarded wings or adult termites</li>
                        </ul>
                        <ul>
                            <li>Any wood that contacts the soil, such as fence posts and foundation elements should be made of pressure treated wood</li>
                        </ul>
                        <ul>
                            <li>Foundation areas should be made accessible for inspection if possible</li>
                        </ul>
                        <ul>
                            <li>Proper grading to direct water away from the structure</li>
                        </ul>
